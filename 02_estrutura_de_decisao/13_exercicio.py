numero_dia_semana = int(input("Digite um número de 1 a 7 para saber o dia de semana: "))
if numero_dia_semana == 1:
    print("1-Domingo")
elif numero_dia_semana == 2:
    print("2-Segunda")
elif numero_dia_semana == 3:
    print("3-Terça")
elif numero_dia_semana == 4:
    print("4-Quarta")
elif numero_dia_semana == 5:
    print("5-Quinta")
elif numero_dia_semana == 6:
    print("6-Sexta")
elif numero_dia_semana == 7:
    print("7-Sabado")
else:
    print("Valor Inválido")
